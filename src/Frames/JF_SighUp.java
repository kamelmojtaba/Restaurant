    private void CityComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CityComboActionPerformed
        // TODO add your handling code here:

        if(CityCombo.getSelectedItem().toString().equals("Dammam"))
        {
            NeighborhoodCombo.removeAllItems();
            NeighborhoodCombo.addItem("AlNuzhah");
            NeighborhoodCombo.addItem("AlWahah");
            NeighborhoodCombo.addItem("AlRayan");
        }
        else if(CityCombo.getSelectedItem().toString().equals("Khobar"))
                {
                    NeighborhoodCombo.removeAllItems();
                    NeighborhoodCombo.addItem("Alyarmouk");
                    NeighborhoodCombo.addItem("Alrawabi");
                    NeighborhoodCombo.addItem("Alolaya");
                }
        else if(CityCombo.getSelectedItem().toString().equals("Jubail"))
        {
                    NeighborhoodCombo.removeAllItems();
                    NeighborhoodCombo.addItem("AlFateh");
                    NeighborhoodCombo.addItem("AlSalmaniya");
                    NeighborhoodCombo.addItem("AlSeef");
        }
        else
        {
                    NeighborhoodCombo.removeAllItems();
                    NeighborhoodCombo.addItem("AlAndalus");
                    NeighborhoodCombo.addItem("AlQadisiyah");
        }
    }//GEN-LAST:event_CityComboActionPerformed

