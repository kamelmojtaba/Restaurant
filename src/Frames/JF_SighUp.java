    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        // TODO add your handling code here:
        sign_up_flag=true;
        String fname,lname,email,password,username;
        fname=FnameText.getText();
        lname=LnameText.getText();
        email=EmailText.getText();
        username=UsernameText.getText();
        password=PasswordText.getText();
       
        String City,neighbor;
        try{
        City=CityCombo.getSelectedItem().toString();
        neighbor=NeighborhoodCombo.getSelectedItem().toString();
        }
        catch(NullPointerException e)
        {
            City="Not Defined";
            neighbor="Not Defined";
        }
        CustomerSighUp user=new CustomerSighUp(fname,lname,email,City,neighbor,username,password);
        ArrayList<String> wrong = new ArrayList<>();

        if (fname.equals("")) {
            wrong.add("Frist Name is Empty");
            sign_up_flag = false;
        }
       
        if (lname.equals("")) {
            wrong.add("Last Name is Empty");
            sign_up_flag = false;
        }

        if (email.equals("")) {
            wrong.add("Email is Empty");
            sign_up_flag = false;
        } else 
        {
            if ((email.contains("@")) && (email.endsWith(".com") || email.endsWith(".sa")))
            {
            } 
            else 
                    {
                     wrong.add("Wrong formatted email");
                     sign_up_flag = false;
                     }
        }
        int m=login(username,password);
        if (username.equals("")) {
            wrong.add("username is Empty");
            sign_up_flag = false;
        }else{
            if( m == 1){
                  wrong.add("Customer already exist! please log in");
                  sign_up_flag = false;
            }
        }
        if (password.equals("")) {
            wrong.add("password is Empty");
            sign_up_flag = false;
        }
        if (sign_up_flag) {
              save(user);  
              setVisible(false);
              new JF_menu(username).setVisible(true);
                
        } 
            else {
            String str = "Please fix the following:";
            for (int i = 0; i < wrong.size(); i++) {
                str += ("\n" + (1 + i) + " ." + wrong.get(i));
            }
            JOptionPane.showMessageDialog(this, str, "warning", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_NextButtonActionPerformed

